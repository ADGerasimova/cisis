-- ============================================================================
-- 083_v3_89_0_apply_temperature_correction.sql
-- ⭐ v3.89.0: Чекбокс «Применять температурную поправку» для барометров.
--
-- Для механических барометров (БАММ-1 и аналоги) температурная поправка
-- обязательна по паспорту прибора (Л82.832.001 ПС, п. 5.4) и считается по
-- формуле из калькулятора. Для цифровых датчиков (Testo 622 и т.п.) эта
-- компенсация выполняется внутри прибора, поэтому повторное её применение
-- калькулятором даёт двойную поправку (~0.03 кПа).
--
-- DEFAULT TRUE обеспечивает обратную совместимость: все существующие
-- барометры продолжат работать как раньше; для цифровых приборов админ
-- снимает галку вручную через форму редактирования оборудования.
-- ============================================================================

ALTER TABLE equipment
    ADD COLUMN apply_temperature_correction BOOLEAN NOT NULL DEFAULT TRUE;

COMMENT ON COLUMN equipment.apply_temperature_correction IS
    'Применять температурную поправку в калькуляторе давления. '
    'TRUE для механических барометров (БАММ и пр.) — обязательна по паспорту. '
    'FALSE для цифровых датчиков с внутренней компенсацией (Testo 622 и т.п.). '
    'По умолчанию TRUE для обратной совместимости.';
